function solveDay3A() {
    let treeProduct = 1;
    treeProduct *= countTrees(1, 1);
    treeProduct *= countTrees(3, 1);
    treeProduct *= countTrees(5, 1);
    treeProduct *= countTrees(7, 1);
    treeProduct *= countTrees(1, 2);
    return treeProduct;

}
function countTrees(slopeX, slopeY) {
    let trees = 0;
    let indexY = 0;
    let indexX = 0;

    // right 3, down 1:

    for (indexY = slopeY; indexY < inData.length; indexY += slopeY) {
        indexX += slopeX;
        if (indexX > 30) {
            indexX -= 31;
        }
        if (inData[indexY][indexX] === "#") {
            trees++
        }
    }
    return (trees)

};

